/**
 * convert_size_number - Function that casts number to specified
 * size of output.
 * @num: Number to be cast to output.
 * @size: the Number to specify cast.
 *
 * Return: num.
 */
long int convert_size_number(long int num, int size)
{
	if (size == S_LONG)
	return (num);

	else if (size == S_SHORT)
		return (num);

	return (num);
}
